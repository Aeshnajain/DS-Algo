import java.util.*;

public class counting
{
    public static void main(String[] args)
    {
        Scanner scn=new Scanner(System.in);
        System.out.println("enter the number");
        int n;
        n=scn.nextInt();
        for(int i=0;i<=n;i++)
        {
            System.out.println(+i);
        }
    }
}
